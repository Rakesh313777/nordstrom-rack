function display(data) {


    var products = document.querySelector("#products")

    products.innerHTML = null

    data.forEach(function (elem) {

        var div = document.createElement("div")
        div.style.cursor = "pointer"
        div.addEventListener("click", function () {
            dis(elem);
        })

        var img = document.createElement("img")
        img.src = elem.img

        var color = document.createElement("h4")
        color.innerText = elem.color

        var brand = document.createElement("h3")
        brand.innerText = elem.brand

        var name = document.createElement("h3")
        name.innerText = elem.name

        var price = document.createElement("p")
        price.innerText = "$" + elem.price

        var div1 = document.createElement("div")

        var p1 = document.createElement("p")
        p1.innerText = elem.price_offer

        var p2 = document.createElement("p")
        p2.innerText = elem.price_cutoff

        var p3 = document.createElement("p")
        p3.innerText = elem.ship

        div1.append(p1, p2)

        div.append(img, color, brand, name, price, div1, p3)

        products.append(div)
    })

}


